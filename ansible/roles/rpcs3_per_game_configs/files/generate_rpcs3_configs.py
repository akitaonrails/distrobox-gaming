def get_game_specific_config(tid: str) -> str | None:
    """Return curated config for known-problematic games, else None."""
    cfg = KNOWN_GAME_CONFIGS.get(tid)
    # `None` value = alias of another title ID; resolve by checking which
    # other title in the dict points to a real config and shares prefix.
    if cfg is None and tid in KNOWN_GAME_CONFIGS:
        # Find sibling with same game (same wiki-id concept, manual mapping)
        # Simple heuristic: scan dict for non-None body and apply if title
        # is one of the documented aliases.
        for other_tid, body in KNOWN_GAME_CONFIGS.items():
            if body is None:
                continue
            # GT6 aliases all map to BCES01893
            if tid in ("BCAS25018", "BCES01977", "BCJS37016", "BCUS98296") and other_tid == "BCES01893":
                return body
    return cfg


def extract_title_id(sfo_path: str, dir_name: str) -> str | None:
    """Extract the disc-format title ID (BLxx12345 / BCxx12345).

    PARAM.SFO sometimes only contains the PSN/trophy ID (NPxx12345) which the
    RPCS3 compat API doesn't index, so we prefer disc IDs and fall back to
    parsing the directory name, which often includes the original disc ID.
    """
    try:
        with open(sfo_path, "rb") as f:
            data = f.read(4096)
    except OSError:
        return None

    # 1. Disc title ID inside PARAM.SFO — best signal
    match = DISC_TITLE_RE.search(data)
    if match:
        return match.group(1).decode("ascii")

    # 2. Disc title ID parsed from the directory name (handles BLUS-30016 etc.)
    for m in DIRNAME_TITLE_RE.finditer(dir_name):
        prefix = m.group(1)
        if prefix in ("BL", "BC"):
            return f"{prefix}US{m.group(2)}" if prefix == "BL" else f"{prefix}ES{m.group(2)}"
    # More forgiving second pass on dir name to catch variants like BLUS30016
    raw = re.search(r"(BL|BC)([A-Z]{2})[-_]?([0-9]{5})", dir_name)
    if raw:
        return f"{raw.group(1)}{raw.group(2)}{raw.group(3)}"

    # 3. Fall back to PSN/trophy ID (rarely matches in compat DB)
    match = PSN_TITLE_RE.search(data)
    return match.group(1).decode("ascii") if match else None


def find_games(ps3_root: str) -> dict:
    """Return dict of {title_id: (game_dir, game_name)} for installed games."""
    games = {}
    if not os.path.isdir(ps3_root):
        return games

    for entry in sorted(os.listdir(ps3_root)):
        game_dir = os.path.join(ps3_root, entry)
        sfo = os.path.join(game_dir, "PS3_GAME", "PARAM.SFO")
        if not os.path.isfile(sfo):
            continue
        tid = extract_title_id(sfo, entry)
        if not tid:
            continue
        # Strip trailing .ps3 directory naming convention
        name = entry[:-4] if entry.endswith(".ps3") else entry
        # Don't overwrite — first occurrence wins
        games.setdefault(tid, (game_dir, name))

    return games


def fetch_status(tid: str, retries: int = 2) -> str:
    """Query RPCS3 API for compatibility status. Returns one of:
    Playable, Ingame, Loadable, Nothing, Unknown."""
    url = RPCS3_API.format(tid=tid)
    for attempt in range(retries + 1):
        try:
            req = urllib.request.Request(
                url, headers={"User-Agent": "ansible-distrobox-gaming/1.0"}
            )
            with urllib.request.urlopen(req, timeout=10) as resp:
                data = json.loads(resp.read().decode("utf-8"))
        except Exception:
            if attempt < retries:
                time.sleep(1)
                continue
            return "Unknown"

        if data.get("return_code") != 0:
            return "Unknown"

        results = data.get("results") or {}
        # API returns multiple regional title IDs; prefer exact match
        if tid in results:
            return results[tid].get("status", "Unknown")
        # Fall back to first result if our exact ID isn't in the response
        if results:
            first = next(iter(results.values()))
            return first.get("status", "Unknown")
        return "Unknown"
    return "Unknown"


def render_config(tid: str, status: str, title: str) -> str | None:
    # Hand-curated overrides take precedence — these are documented fixes
    # for games that misbehave with the generic templates.
    curated = get_game_specific_config(tid)
    if curated is not None:
        return curated
    if status == "Ingame":
        return COMMON_INGAME_OVERRIDES.format(status=status, title=title)
    if status == "Loadable":
        return LOADABLE_OVERRIDES.format(status=status, title=title)
    # Playable / Nothing / Unknown -> no config written
    return None


def main(argv: Iterable[str]) -> int:
    args = list(argv)
    if not args:
        print(__doc__)
        return 1

    ps3_root = args[0]
    dest = DEFAULT_DEST
    dry_run = False
    force = False

    i = 1
    while i < len(args):
        a = args[i]
        if a == "--dest" and i + 1 < len(args):
            dest = args[i + 1]
            i += 2
        elif a == "--dry-run":
            dry_run = True
            i += 1
        elif a == "--force":
            force = True
            i += 1
        else:
            print(f"Unknown arg: {a}", file=sys.stderr)
            return 1

    games = find_games(ps3_root)
    if not games:
        print(f"No PS3 games found in {ps3_root}", file=sys.stderr)
        return 1

    if not dry_run:
        os.makedirs(dest, exist_ok=True)

    print(f"Found {len(games)} PS3 games. Querying RPCS3 compatibility API...")
    print(f"Destination: {dest}")
    if dry_run:
        print("DRY RUN — no files will be written")
    print()

    counts = {"written": 0, "skipped_existing": 0, "skipped_status": 0, "errors": 0}
    by_status: dict[str, int] = {}

    for tid, (_, name) in sorted(games.items()):
        status = fetch_status(tid)
        by_status[status] = by_status.get(status, 0) + 1

        body = render_config(tid, status, name)
        if body is None:
            print(f"  {tid}  {status:<10}  {name[:60]}  (no config needed)")
            counts["skipped_status"] += 1
            continue

        config_path = os.path.join(dest, f"{tid}_config.yml")
        if os.path.exists(config_path) and not force:
            print(f"  {tid}  {status:<10}  {name[:60]}  (existing — keep)")
            counts["skipped_existing"] += 1
            continue

        if dry_run:
            print(f"  {tid}  {status:<10}  {name[:60]}  (would write)")
            counts["written"] += 1
            continue

        try:
            with open(config_path, "w") as f:
                f.write(body)
            print(f"  {tid}  {status:<10}  {name[:60]}  (written)")
            counts["written"] += 1
        except OSError as e:
            print(f"  {tid}  ERROR: {e}", file=sys.stderr)
            counts["errors"] += 1

        # Be polite to the API
        time.sleep(0.2)

    print()
    print("Status breakdown:")
    for status, n in sorted(by_status.items(), key=lambda x: -x[1]):
        print(f"  {status:<10} {n}")
    print()
    print(
        f"Complete: {counts['written']} written, "
        f"{counts['skipped_status']} skipped (status fine), "
        f"{counts['skipped_existing']} skipped (already exist), "
        f"{counts['errors']} errors"
    )
    return 0 if counts["errors"] == 0 else 1


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
