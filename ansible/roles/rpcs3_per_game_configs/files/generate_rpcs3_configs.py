#!/usr/bin/env python3
"""
Generate RPCS3 per-game custom configs for installed PS3 games.

Walks the PS3 ROM root, parses PARAM.SFO for each game's title ID,
queries the RPCS3 compatibility API, and writes a custom config YAML
to ~/.config/rpcs3/custom_configs/<TITLE_ID>_config.yml only when the
game's compatibility status indicates it benefits from tuning.

Strategy:
  - Playable: skip (defaults work)
  - Ingame:   apply common rendering fixes (Write Color Buffers etc.)
  - Loadable: apply aggressive fixes (likely won't help much)
  - Nothing:  skip (no point)

The generated config only sets keys that DIFFER from RPCS3 defaults.
Existing user-edited configs are NOT overwritten unless --force is given.

Usage:
    python3 generate_rpcs3_configs.py /path/to/ps3-roms \\
        [--dest ~/.config/rpcs3/custom_configs] \\
        [--dry-run] [--force]
"""

import json
import os
import re
import struct
import sys
import time
import urllib.request
from typing import Iterable

DEFAULT_DEST = os.path.expanduser("~/.config/rpcs3/custom_configs")
RPCS3_API = "https://rpcs3.net/compatibility?api=v1&g={tid}"
DISC_TITLE_RE = re.compile(rb"(?:^|\x00)((?:BL|BC)[A-Z]{2}[0-9]{5})")
PSN_TITLE_RE = re.compile(rb"(?:^|\x00)(NP[A-Z]{2}[0-9]{5})")
DIRNAME_TITLE_RE = re.compile(r"(BL|BC|NP)[A-Z-]?[A-Z]{1,2}[-_]?([0-9]{5})")


